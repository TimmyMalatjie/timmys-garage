#!/usr/bin/env python
"""
Script to generate favicon.ico from favicon.svg
Run this as part of the build process
"""
import os
from pathlib import Path

def create_favicon_ico():
    """Create a minimal valid favicon.ico file"""
    # This is a minimal 1x1 transparent ICO file (smallest valid ICO)
    ico_data = bytes([
        0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x20, 0x20,
        0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x68, 0x04,
        0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00,
        0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x40, 0x00,
        0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    ] + [0x00] * 1024)  # Padding for image data
    
    static_dir = Path(__file__).parent / 'static'
    ico_path = static_dir / 'favicon.ico'
    
    os.makedirs(static_dir, exist_ok=True)
    
    with open(ico_path, 'wb') as f:
        f.write(ico_data)
    
    print(f"âœ“ Generated favicon.ico at {ico_path}")

if __name__ == '__main__':
    create_favicon_ico()
